package variables;

public class UnderstandingVariables {
    public static void main(String[] args) {

        // Variable declarations and assignment with an initial value


          int age = 45;
        System.out.println( age );  // 45
        // variable declaration - it allocates the memory

        String name;
        //variable assignment
        name = " John ";

        System.out.println(name); // John

        name = " Mike";

        System.out.println(name);// Mike
        // this is how we reassign age

        age = 21;

        System.out.println(age);






    }
}
