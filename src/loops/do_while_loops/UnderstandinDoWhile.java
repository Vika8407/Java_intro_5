package loops.do_while_loops;

public class UnderstandinDoWhile {
    public static void main(String[] args) {
        //---------Do While Loop---------//
        int i = 0;
        do {
            System.out.println(i);
            i++;
        }
        while ( i < 5 );

        //---------While loop----------//
        int j = 0;
        while (j < 5){
            System.out.println(j);
            j++;
        }
        System.out.println(" End of the program ! ");
    }
}
