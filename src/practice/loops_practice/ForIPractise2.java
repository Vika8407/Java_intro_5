package practice.loops_practice;

public class ForIPractise2 {
    public static void main(String[] args) {

        System.out.println(" \n Hardcoded print statements \n");

        System.out.println(" Number : " + " 1 ");
        System.out.println(" Number : " + " 2 ");
        System.out.println(" Number : " + " 3 ");
        System.out.println(" Number : " + " 4 ");
        System.out.println(" Number : " + " 5 ");

        System.out.println(" \n Fori number counter v1 \n");

        for( int i = 1; i < 6; i++){// i n = 1, 2, 3, 4, 5
            System.out.println(" Number : " + i);
        }

        System.out.println(" \n Fori number counter v2 \n");

        for( int i = 0; i < 5; i++){// i n = 0,1, 2, 3, 4
            System.out.println(" Number : " + (i + 1));
        }


    }
}
